package characters

import (
	"assa.com/put.pixel/lib/mlib"
	"assa.com/put.pixel/lib/ogl"
)

type Chr struct {
	Type  int
	X     int
	Y     int
	Color byte
	shape [][]int
	frame int
	wH    int
	wW    int
}

func GetNew(code rune, color byte) *Chr {
	ch := &Chr{}
	ch.wH = ogl.GetWindowHeight()
	ch.wW = ogl.GetWindowWidth()
	ch.init(code, color)
	return ch
}

func (ch *Chr) init(code rune, color byte) {
	ch.X = 0
	ch.Y = 0
	ch.Type = getTypeByCode(code)
	ch.initType()
	ch.Color = color
}

