func (ch *Chr) colorLogic() {
	if ch.Color < 0 {
		ch.Y = ch.wH + mlib.Rand(50)
		ch.Color = byte(mlib.Rand(255))
		return
	}

	if mlib.Rand(5) == 2 {
		ch.Color--
	}
}

